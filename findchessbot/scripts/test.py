def fcb_IK(gripper_linear_x,  gripper_linear_y,  gripper_linear_z,  gripper_angular_yaw):
    x = gripper_linear_x
    y = gripper_linear_y
    z = gripper_linear_z
    yaw = gripper_angular_yaw

    L3 = 315.0
    L12 = 383.25
    H1 = 125.0
    H3 = 65.0
    H4 = 190.0

    C3 = ((x*x)+(y*y)-(L12*L12)-(L3*L3)) / (2*L12*L3)
    S3 = -1.0 * sqrt(1-(C3*C3))
    q3 = atan2(S3,C3)

    L3S3 = L3*S3
    L123C3 = L12 + (L3*C3)

    S1 = (-L3S3*x) + (L123C3*y)
    C1 = (L3S3*y) + (L123C3*x)
    q1 = atan2(S1,C1)
    q4 = yaw - q1 - q3
    q2 = z + H4 - H3 - H1

    print(q1)
    print(q2)
    print(q3)
    print(q4)
    return 0
    

